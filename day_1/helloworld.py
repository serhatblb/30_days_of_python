print(2 + 3) # toplama (+)
print(2 - 3) # cikarma (-)
print(2 * 3) # carpma (*)
print(2 / 3) # bolme (/)
print(2 // 3) # tam bolme (//)
print(2 ** 3) # us alma (*)
print(2 % 3) # mod alma (%)

